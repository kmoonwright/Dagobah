-- GENERAL GUIDE
-- Order of Operations
  -- 1. FROM
  -- 2. JOIN
  -- 3. WHERE
  -- 4. GROUP BY
  -- 5. HAVING
  -- 6. SELECT
  -- 7. ORDER BY
  -- 8. LIMIT/OFFSET




-- EXAMPLE:
SELECT column1, column2...
FROM table1
WHERE column4 = 1 AND column5 = 5
GROUP BY something
HAVING count(*) > 1
ORDER BY ASC or DESC


